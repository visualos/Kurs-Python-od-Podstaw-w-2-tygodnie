a = 10
b = 6

result1 = a + b      # 16
result2 = a - b      # 4
result3 = a * b      # 60
result4 = a / b      # 1.6666666666666667
result5 = a // b     # dzielenie dające tylko liczbę całkowitą jako wynik (floor division): 1
result6 = a % b      # reszta z dzielenia przez sześć: 4
result7 = a ** b     # 10^6 = 100000

print(result1)
print(result2)
print(result3)
print(result4)
print(result5)
print(result6)
print(result7)
